namespace Bonsai.Harp
{
    /// <summary>
    /// Specifies the state of an LED on the Harp device.
    /// </summary>
    public enum LedState : byte
    {
        /// <summary>
        /// Specifies that the LED is on.
        /// </summary>
        On = 0,

        /// <summary>
        /// Specifies that the LED is off.
        /// </summary>
        Off = 1
    }
}
